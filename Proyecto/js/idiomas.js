// funcion para cambiar el idioma
function changeLanguage() {
  //conversion
  const lang = document.documentElement.lang === "es" ? "en" : "es";
  document.documentElement.lang = lang;

  // cambiar texto de los elementos con data-translate
  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    //elementos html sean tradudidos por la llave
    element.innerHTML = translations[lang][key] || element.innerHTML;
  });

  document.querySelectorAll("[data-translate]").forEach((element) => {
    const key = element.getAttribute("data-translate");
    if (element.hasAttribute("placeholder")) {
      element.setAttribute(
        "placeholder",
        translations[lang][key] || element.getAttribute("placeholder")
      );
    }
    if (
      element.tagName.toLowerCase() === "input" &&
      element.type === "button"
    ) {
      element.value = translations[lang][key] || element.value;
    }
  });
}
